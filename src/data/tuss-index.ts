// Mapa para busca rápida por código
export const TUSS_MAP = new Map<string, string>(
  TUSS_DATA.map((t) => [t.code, t.description])
);

// Função de busca
export function searchTuss(query: string, limit = 50): TussEntry[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return TUSS_DATA
    .filter((t) => t.code.includes(q) || t.description.toLowerCase().includes(q))
    .slice(0, limit);
}

// Função para obter descrição por código
export function getTussDescription(code: string): string | undefined {
  return TUSS_MAP.get(code);
}

